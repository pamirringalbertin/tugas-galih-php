<?php
$bilangan = 10;
if ($bilangan % 2 == 0) {
  echo "Bilangan $bilangan adalah bilangan genap.";
} else {
  echo "Bilangan $bilangan adalah bilangan ganjil.";
}
?>
Program ini akan meminta pengguna untuk memasukkan bilangan bulat, kemudian mengecek apakah bilangan tersebut genap atau ganjil menggunakan operasi modulo (%). Jika hasil dari bilangan tersebut dibagi 2 adalah 0, maka program akan mencetak bahwa bilangan tersebut adalah bilangan genap. Jika tidak, maka program akan mencetak bahwa bilangan tersebut adalah bilangan ganjil.